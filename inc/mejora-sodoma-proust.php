<h2>3. El testimonio de los profetas: la verdadera anatomía
de la destrucción</h2>

Si la lectura proustiana y la literatura del exilio ofrecen una
dimensión simbólica del arquetipo, los profetas de Israel y de
la Restauración añaden una dimensión diagnóstica que trasciende
las lecturas reduccionistas del relato.

<h3>Más allá de una sola transgresión</h3>

La interpretación popular suele reducir la destrucción de Sodoma
a un único tipo de pecado. Sin embargo, el profeta Ezequiel
ofrece un diagnóstico notablemente más complejo:

<blockquote>"He aquí que esta fue la maldad de Sodoma tu hermana:
soberbia, saciedad de pan, y abundancia de ociosidad tuvieron
ella y sus hijas; y no fortaleció la mano del afligido y del
menesteroso" (Ezequiel 16:49).</blockquote>

El catálogo es revelador: orgullo, complacencia material,
ociosidad y, sobre todo, indiferencia ante el sufrimiento ajeno.
La inmoralidad sexual era una manifestación, pero no la raíz.
El pecado radical de Sodoma fue la deshumanización del otro,
la erosión de la comunidad como tejido de responsabilidad mutua.

<h3>El rechazo a los profetas</h3>

El profeta José Smith identificó la causa con precisión: los
habitantes de Sodoma y Gomorra "fueron destruidos por haber
rechazado a los profetas" (Enseñanzas del Profeta José Smith,
pág. 213). Esta lectura conecta el relato antiguo con un patrón
que se repite a lo largo del ciclo de la historia de la salvación:
Dios envía mensajeros, la sociedad los rechaza, y sobreviene la
destrucción.

El presidente Marion G. Romney, en su discurso "The Tragic Cycle"
(Conferencia General, octubre de 1977), trazó explícitamente
este arco: Noé y el diluvio, Sodoma y Gomorra, Jerusalén, las
civilizaciones jaredita y nefita. En cada caso el ciclo es
idéntico: advertencia profética, rechazo, destrucción.

<h3>La Traducción de José Smith: claridad donde hay ambigüedad</h3>

La Traducción de José Smith (JST) de Génesis 19 resuelve una de
las dificultades más perturbadoras del relato: la aparente oferta
de Lot de entregar a sus hijas a la turba. La JST aclara que la
turba exigía tanto a los visitantes como a las hijas, que Lot se
negó, y que los ángeles intervinieron cegando a los agresores.

Esta corrección es importante porque preserva la integridad moral
de Lot como "hombre justo" (2 Pedro 2:7-8), y al mismo tiempo
revela la profundidad de la depravación de Sodoma: la violencia
colectiva contra el inocente no conocía límites.

<h3>El presidente Boyd K. Packer y el paralelismo contemporáneo</h3>

En 2004, el presidente Boyd K. Packer declaró: "No sé nada en
la historia de la Iglesia o del mundo que se compare con nuestras
circunstancias actuales. No sucedió nada en Sodoma y Gomorra que
excediera la maldad y depravación que nos rodea ahora... En
Sodoma y Gomorra estas cosas ocurrían solo en ese lugar. Ahora
se esparcen por todo el mundo".

El presidente Gordon B. Hinckley hizo eco de esta advertencia:
"Todos los pecados de Sodoma y Gomorra afligen a nuestra
sociedad. Jamás nuestra gente joven ha enfrentado más grandes
desafíos".

Leer el arquetipo de Sodoma exclusivamente como alegoría
literaria es leerlo incompleto. Los profetas lo presentan como
diagnóstico y advertencia: el colapso de una civilización
comienza mucho antes del fuego, en la erosión silenciosa de
la compasión, el orgullo institucionalizado, y la sordera
voluntaria ante la voz de los mensajeros de Dios.
