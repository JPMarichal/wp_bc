/*
TÍTULO: Lot: de la tienda a la puerta — anatomía de una progresión decadente

EXTRACTO: Lot no cayó en Sudam.com/en/un/día. Su descenso
tuvo etapas: primero miró hacia la llanura, luego plantó su
tienda hacia Sodoma, después se mudó a la ciudad, más tarde
se sentó en la puerta como juez, y finalmente tuvo que ser
arrastrado por ángeles para salvar su vida. Cada etapa es una
lección sobre cómo la familiaridad con el pecado nos insensibiliza.

=== CONTENIDO ===

<!-- wp:paragraph -->
<p>Hay personajes en las Escrituras cuya función principal parece ser la de servir como contraejemplo. Lot, el sobrino de Abraham, es uno de ellos. Pero la tragedia de Lot no es una caída repentina ni un estallido de rebelión abierta contra Dios. Es una historia de erosión gradual, de decisiones pequeñas y aparentemente razonables que, acumuladas, terminan por definir un destino.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La historia de Lot es el relato de cómo un hombre justo (2 Pedro 2:7-8) puede terminar sentado en la puerta de una ciudad condenada, y después tan aferrado a ella que los ángeles tienen que tomarlo de la mano y arrastrarlo para que se salve.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">1. La mirada: Lot alza los ojos hacia la llanura</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>La progresión comienza en Génesis 13, cuando Abraham y Lot se separan para resolver el conflicto entre sus pastores. Abraham, en un gesto de generosidad patriarcal, ofrece a Lot la primera opción del territorio:</p>
<!-- /wp:paragraph -->

<!-- wp:quote -->
<blockquote class="wp-block-quote"><!-- wp:paragraph -->
<p>"Y alzó Lot sus ojos y vio toda la llanura del Jordán, que toda ella era de riego, antes que destruyese Jehová a Sodoma y a Gomorra, como el huerto de Jehová, como la tierra de Egipto entrando en Zoar" (Génesis 13:10).</p>
<!-- /wp:paragraph --></blockquote>
<!-- /wp:quote -->

<!-- wp:paragraph -->
<p>Lot <strong>elige con los ojos</strong>. Ve fertilidad, agua, prosperidad. Su decisión es racional desde una perspectiva puramente económica: el valle promete riqueza. Pero el texto añade un detalle ominoso: "Mas los hombres de Sodoma eran malos y pecadores delante de Jehová en gran manera" (Génesis 13:13). Lot sabe dónde va. La advertencia está ahí, implícita en el texto, pero él la ignora.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>No hay maldad en su decisión inicial. Hay, más bien, una priorización de lo visible sobre lo invisible, de lo material sobre lo espiritual. Es una elección que cualquier persona práctica habría tomado.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">2. La tienda: Lot pone su campamento hacia Sodoma</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El versículo 12 dice: "Lot se asentó en las ciudades de la llanura y fue poniendo sus tiendas hasta Sodoma". </p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La frase hebrea sugiere un movimiento progresivo, un desplazamiento gradual. Lot no se muda directamente a Sodoma; primero pone su tienda en las cercanías, luego un poco más cerca, hasta que la ciudad se convierte en el centro gravitacional de su vida.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El élder L. Tom Perry enseñó: "La mayoría de los problemas a los que Lot se enfrentó más tarde en la vida, y fueron varios, pueden remontarse a su primera decisión de colocar la puerta de su tienda en dirección a Sodoma". Luego contrastó con Abraham: "Aunque no lo sé, personalmente creo que la puerta de la tienda de Abraham miraba hacia el altar que él construyó para el Señor" ("The Power to Deliver", Conferencia General, abril de 2012).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El contraste es pedagógico: Abraham edificó un altar; Lot plantó su tienda "hacia Sodoma". Uno orientó su vida hacia el culto; el otro, hacia la comodidad y la oportunidad económica. La dirección de la puerta de una tienda parece un detalle menor, pero es un marcador del corazón.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">3. La ciudad: Lot mora en Sodoma</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>En Génesis 14:12 leemos: "Tomaron también a Lot, hijo del hermano de Abram, que <strong>moraba en Sodoma</strong>". La progresión se ha completado: ya no está en las afueras, vive dentro.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Es interesante notar que cuando Abraham rescata a Lot de la coalición de reyes (Génesis 14), Lot no decide mudarse. Regresa a Sodoma. La ciudad se ha convertido en su hogar. La familiaridad ha reemplazado a la cautela.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Aquí vemos un patrón psicológico bien documentado: la exposición repetida a un estímulo reduce la respuesta de alarma. Lo que al principio parecía peligroso o moralmente comprometido, con el tiempo se vuelve normal. Lot ya no "va hacia" Sodoma; Sodoma es su dirección permanente.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">4. La puerta: Lot es juez en Sodoma</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El detalle más revelador de la historia aparece en Génesis 19:1: "Llegaron, pues, los dos ángeles a Sodoma a la caída de la tarde; y <strong>Lot estaba sentado a la puerta de Sodoma</strong>".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>En el mundo antiguo, la puerta de la ciudad no era simplemente una entrada. Era el centro administrativo y judicial: allí se reunían los ancianos, se dirimían los pleitos legales, se cerraban los contratos y se administraba justicia (Rut 4:1-11; Proverbios 31:23). Sentarse a la puerta era ejercer liderazgo cívico.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Lot, el extranjero que vino como residente temporal, se ha convertido en parte del establecimiento de Sodoma. Es juez. Es líder. Tiene un asiento en el consejo de la ciudad. La ironía es trágica: el hombre que eligió Sodoma por su prosperidad ahora es responsable de administrar justicia en una sociedad que textualmente "era mala y pecadora delante de Jehová en gran manera".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Los propios sodomitas lo reconocen y se lo reprochan cuando Lot intenta proteger a los ángeles: "Vino este aquí para habitar como extranjero, ¿y habrá de erigirse en juez?" (Génesis 19:9). Le recuerdan su condición de forastero, pero también confirman que él ha asumido ese rol. Lot tiene suficiente autoridad moral para reprenderlos, pero no suficiente para que le hagan caso.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Sentarse en la puerta de Sodoma representa el punto más alto de la carrera worldly de Lot... y el punto de mayor peligro espiritual. Ha llegado a la cima de una sociedad que está a punto de ser destruida.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">5. El umbral: Lot se queda y los ángeles lo arrastran</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Cuando los ángeles advierten a Lot que la destrucción es inminente, su reacción revela cuán profundamente la ciudad lo ha atrapado: "Entonces Lot salió y habló a sus yernos, los que habían de tomar sus hijas, y dijo: Levantaos, salid de este lugar, porque Jehová va a destruir esta ciudad. Mas pareció a sus yernos como si se burlara" (Génesis 19:14).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Sus propios yernos piensan que está bromeando. Su testimonio tiene tan poca credibilidad en su propia casa que nadie lo toma en serio. El hombre sentado en la puerta se ha convertido en una voz que clama en el desierto de su propia familia.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Luego viene el momento más condenatorio: "Y al rayar el alba, los ángeles daban prisa a Lot, diciendo: Levántate, toma tu mujer y tus dos hijas que se hallan aquí, para que no perezcas en el castigo de la ciudad. Y deteniéndose él, los asieron aquellos varones de la mano, y a su mujer y a sus dos hijas, por misericordia de Jehová hacia él, y lo sacaron y lo pusieron fuera de la ciudad" (Génesis 19:15-16).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p><strong>"Y deteniéndose él."</strong> Lot se queda paralizado. Los ángeles tienen que <strong>tomarlo de la mano</strong> para sacarlo. No es un héroe de la fe que sale confiado; es un hombre que duda incluso cuando el azufre está por caer. Es rescatado no por su fe, sino por la misericordia de Dios que recuerda a Abraham (Génesis 19:29).</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">6. La cueva: el trágico epílogo</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El final de Lot es silencioso y sombrío. Después de la destrucción, "subió Lot de Zoar y habitó en el monte, y sus dos hijas con él; porque tuvo miedo de quedarse en Zoar; y habitó en una cueva, él y sus dos hijas" (Génesis 19:30).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>De la tienda abierta a la llanura fértil, pasando por la ciudad próspera y el asiento de juez en la puerta, hasta terminar en una cueva en el monte. El arco de la vida de Lot es un arco descendente: riqueza, poder, liderazgo, y finalmente aislamiento y pobreza.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La Traducción de José Smith (JST) de Génesis 19 describe las acciones de las hijas de Lot como inicuas, y el relato explica el origen de los moabitas y amonitas, naciones que más tarde se convertirían en adversarios de Israel. Sin embargo, de esa línea manchada surgiría Rut la moabita, bisabuela del rey David y antepasada de Jesucristo. Incluso en la tragedia más oscura, la gracia de Dios teje redención.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Lecciones para el presente</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>La progresión de Lot es peligrosa precisamente porque es sutil. Rara vez alguien decide "volverse malo" de un día para otro. El patrón es más bien:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li>Primero, se permite una mirada hacia lo que atrae.</li>
<!-- /wp:list-item -->
<!-- wp:list-item -->
<li>Luego, se orienta la vida hacia ello (la tienda).</li>
<!-- /wp:list-item -->
<!-- wp:list-item -->
<li>Después, se cruza el umbral para vivir dentro.</li>
<!-- /wp:list-item -->
<!-- wp:list-item -->
<li>Más tarde, se participa del sistema, se asciende en él.</li>
<!-- /wp:list-item -->
<!-- wp:list-item -->
<li>Finalmente, cuando llega la advertencia, uno se queda paralizado, incapaz de soltar lo que tanto le costó construir.</li>
<!-- /wp:list-item --></ul>
<!-- /wp:list -->

<!-- wp:paragraph -->
<p>La pregunta que el relato deja en el aire no es si estamos en Sodoma, sino hacia dónde mira la puerta de nuestra tienda. Y si los ángeles vinieran hoy a advertirnos, ¿tendrían que tomarnos de la mano para sacarnos, o saldríamos corriendo?</p>
<!-- /wp:paragraph -->
