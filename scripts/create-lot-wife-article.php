<!-- wp:paragraph -->
<p>De todas las advertencias que el Salvador pronunció, una de las más breves y enigmáticas es también una de las más inquietantes. En medio de un discurso sobre los últimos días, Lucas registra que Jesús dijo simplemente: "Acordaos de la mujer de Lot" (Lucas 17:32). Sin explicación, sin desarrollo. Tres palabras en español, cinco en el griego original, que presuponen que sus oyentes conocían bien la historia y que comprendían inmediatamente por qué debían recordarla. Dos mil años después, esa breve frase sigue resonando como un llamado a examinar nuestra relación con el pasado, con la fe y con la disposición a soltar lo que debemos dejar atrás.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La historia a la que Jesús se refiere es bien conocida. Lot, su esposa y sus dos hijas habían sido sacados de Sodoma por ángeles momentos antes de que la ciudad fuera destruida. Las instrucciones habían sido inequívocas: "Escapa por tu vida; no mires tras ti, ni pares en toda la llanura; escapa al monte, no sea que perezcas" (Génesis 19:17). Pero en algún momento durante la huida, en circunstancias que el texto no detalla, "la esposa de Lot miró atrás, a espaldas de él, y se volvió estatua de sal" (Génesis 19:26). Eso es todo lo que las Escrituras dicen directamente sobre ella. No se nos da su nombre, ni sus palabras, ni sus motivos. Solo el hecho escueto de su mirada y su transformación.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">La herencia textual: ¿qué clase de mirada fue esa?</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El primer indicio de que esta historia es más sutil de lo que parece está en la palabra hebrea que el texto de Génesis emplea para decir que la esposa de Lot "miró". El verbo utilizado no describe una mirada casual o un vistazo rápido hacia atrás. En su contexto, transmite la idea de una contemplación detenida, un mirar con atención, incluso con anhelo. La esposa de Lot no echó un vistazo: fijó la mirada. Y esa fijación, según el presidente Jeffrey R. Holland, del Cuórum de los Doce Apóstoles, revelaba lo que realmente ocurría en su corazón: "Aparentemente, lo malo que hizo no fue solo mirar atrás, sino que lo que su corazón deseaba era volverse atrás. Parece que aun cuando ya había salido de los límites de la ciudad, echaba de menos lo que Sodoma y Gomorra le habían ofrecido".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El análisis del presidente Holland, presentado en un devocional de la Universidad Brigham Young en 2009 y posteriormente publicado en la revista <em>Liahona</em> bajo el título "Lo mejor aún está por venir", constituye la reflexión más penetrante sobre este pasaje en la literatura SUD contemporánea. Holland identifica la raíz del problema no en el acto físico de volver la cabeza, sino en la postura del corazón: "Es posible que la esposa de Lot haya mirado atrás con resentimiento hacia el Señor por lo que Él le mandaba dejar tras de sí. Así que no se trata de que ella mirara hacia atrás, sino de que haya mirado con ansia de volver; en suma, su apego al pasado tuvo en ella una influencia mayor que su confianza en el futuro. Aparentemente, eso fue al menos parte de su pecado".</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">La casa de veraneo en Babilonia</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El élder Neal A. Maxwell, también del Cuórum de los Doce Apóstoles, ofreció una imagen memorable para describir esta misma condición espiritual. Hablando de aquellos que profesan seguir a Dios pero mantienen un pie en el mundo, Maxwell dijo que son personas que saben que deben tener su residencia principal en Sion, "pero todavía esperan mantener una casa de veraneo en Babilonia". La esposa de Lot, sugiere Holland, ejemplifica precisamente ese dilema: había aceptado salir de Sodoma con los pies, pero su corazón nunca terminó de irse. Quería la seguridad de la huida sin el costo de la separación.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Este concepto resuena con la advertencia que el Señor daría siglos después en una revelación moderna. En Doctrina y Convenios 133, en el contexto de reunir a Israel y huir de la "Babilonia espiritual", el Señor ordena: "No miréis hacia atrás, no sea que os sobrevenga una destrucción repentina" (D. y C. 133:14–15). El lenguaje es casi idéntico al de Génesis 19. Los manuales de la Iglesia han notado explícitamente esta conexión, señalando que la instrucción de "no mirar atrás" en D. y C. 133 alude directamente al relato de Lot. La "Babilonia espiritual" es cualquier sistema de creencias o prácticas que nos aleja de Dios —y, como Sodoma, ejerce una atracción magnética sobre aquellos que no han cortado verdaderamente los lazos.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">El pasado como prisión: la lección de Pablo</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El apóstol Pablo ofrece el contraste más claro con la esposa de Lot. En su carta a los filipenses, después de repasar todos sus credenciales y logros como fariseo —su linaje, su educación, su celo—, declara que todo aquello le parece "basura" comparado con el conocimiento de Cristo. Y luego escribe una de las declaraciones más poderosas del Nuevo Testamento sobre la dirección de la vida cristiana: "olvidando ciertamente lo que queda atrás, y extendiéndome a lo que está delante, prosigo a la meta, al premio del supremo llamamiento de Dios en Cristo Jesús" (Filipenses 3:13–14). Donde la esposa de Lot miró hacia atrás con anhelo, Pablo miró hacia adelante con determinación. Donde ella se aferró a lo que perdía, él soltó lo que tenía. Holland resume el contraste con una frase incisiva: "No hay ahí una esposa de Lot; no se mira atrás hacia Sodoma y Gomorra".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Pablo, sin embargo, no era ajeno a la tentación de mirar atrás. Antes de su conversión, era Saulo de Tarso, un perseguidor de la Iglesia. Dejar atrás esa identidad —y el prestigio que le otorgaba entre sus contemporáneos— requirió una transformación radical. La diferencia entre él y la esposa de Lot no es que Pablo fuera inmune a la nostalgia, sino que había llegado a comprender que lo que Dios tenía para él era infinitamente superior a lo que dejaba. Como dice Holland: "La fe siempre señala hacia el futuro. La fe está siempre relacionada con bendiciones, verdades y acontecimientos del futuro que tendrán efecto positivo en nuestra vida".</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Fe para el futuro</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El núcleo teológico del análisis de Holland sobre la esposa de Lot es que su pecado fundamental fue la falta de fe. No simplemente una falta de fe genérica, sino una incapacidad concreta para confiar en que Dios tenía algo mejor preparado para ella que lo que estaba dejando atrás. "Una forma más teológica de referirnos a la esposa de Lot", escribe Holland, "sería decir que no tuvo fe, que dudó del poder del Señor para darle algo mejor de lo que ya tenía. Al parecer, pensó que nada de lo que le esperaba podía ser de ninguna manera mejor que lo que dejaba atrás".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Esta es, quizás, la lección más penetrante para el creyente contemporáneo. La fe no es solo creer en Dios o aceptar doctrinas; es confiar en que el futuro que Dios ofrece es mejor que el pasado que debemos soltar. Holland lo expresa con claridad: "La fe es para el futuro. La fe pone los cimientos en el pasado pero nunca anhela quedarse allá. La fe confía en que Dios tiene grandes cosas reservadas para cada uno de nosotros y en que Cristo es en verdad el 'sumo sacerdote de las cosas buenas por venir' (Hebreos 9:11)".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La esposa de Lot no es condenada por tener recuerdos o por valorar su vida pasada. Es condenada porque su apego al pasado —a una ciudad que Dios había juzgado— fue más fuerte que su confianza en el futuro que Dios le ofrecía. Su pecado no fue la nostalgia, sino la desconfianza. Como advierte Holland: "El pasado es para aprender de él, pero no para vivir en él. Miramos atrás con el deseo de reclamar las brasas de las experiencias radiantes, pero no las cenizas".</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">El testimonio de la tierra: Jebel Usdum y la estatua de sal</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>La geografía del mar Muerto añade una dimensión peculiar a esta historia. El <em>Diccionario de la Biblia de Hastings</em> señala que en el extremo suroeste del mar Muerto existe una cadena de acantilados de seis millas de largo y 600 pies de alto llamada Jebel Usdum, "el monte de Sodoma". Estos acantilados están formados por cristales de sal de roca, cubiertos de caliza y yeso, y están "curiosamente surcados y desgastados, de modo que a veces se asemejan a una figura humana". <em>Easton's Bible Dictionary</em>, por su parte, menciona una formación específica conocida localmente como "Bint Sheik Lot" (la hija del Sheik Lot), "una aguja de roca alta y aislada que realmente guarda un curioso parecido con una mujer árabe con un niño sobre el hombro".</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Estos fenómenos naturales no necesariamente contradicen la narración bíblica, pero ofrecen un contexto fascinante. Es posible que una mujer real —atrapada en la destrucción— quedara cubierta por sales y sedimentos, de modo que su cuerpo momificado se incrustara en las sales del mar Muerto. O tal vez la tradición oral, al pasar de generación en generación, asoció estas formaciones de sal con la historia de la mujer que miró atrás. En cualquier caso, el paisaje mismo del mar Muerto se convirtió en un monumento perpetuo a la advertencia de Jesús. El manual del Seminario del Antiguo Testamento señala que "la mayoría de los eruditos coinciden en que el sitio donde es más probable que haya estado Sodoma ahora está cubierto por la parte sur del mar Muerto, un cuerpo de agua con un alto contenido de sal". El mar que cubrió las ciudades de la llanura también preservó, en sus costas, el recordatorio visual de lo que significa mirar atrás cuando Dios dice "adelante".</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Conclusión</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Cuando Jesús dijo "Acordaos de la mujer de Lot", no estaba dando una clase de historia del Antiguo Testamento. Estaba ofreciendo una clave para la vida de discipulado. En el contexto de Lucas 17, hablaba de los días del Hijo del Hombre, de la urgencia de estar preparados, de la necesidad de soltar las ataduras que nos amarran a un mundo que pasa. Su advertencia es tan relevante hoy como lo fue entonces: hay momentos en que la obediencia requiere no solo moverse hacia adelante, sino también soltar lo que dejamos atrás. Mirar atrás con anhelo, con resentimiento, con la esperanza secreta de regresar, no es una debilidad menor. Es una forma de incredulidad.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La esposa de Lot no tenía que haber muerto. Los ángeles la sacaron de Sodoma. Lot y sus hijas sobrevivieron. La puerta estaba abierta, el camino estaba despejado. Su error no fue mirar hacia atrás, sino no haber confiado lo suficiente en el futuro de Dios como para no desear regresar. Al acordarnos de ella, recordamos que la fe no es para el pasado. La fe es —y siempre será— para el futuro.</p>
