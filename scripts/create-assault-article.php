<p>El episodio más intenso y revelador del relato de Sodoma y Gomorra es el asalto a la casa de Lot. No se trata de un mero incidente de violencia callejera, sino de la manifestación pública y corporativa de la verdadera naturaleza de Sodoma. Cuando la ciudad entera —"desde el más joven hasta el más viejo" (Génesis 19:4)— rodea la casa de un juez local para violar a sus huéspedes, lo que se despliega ante el lector no es un acto de depravación privada sino una declaración política: en Sodoma, la violencia contra el extranjero es ley. La intervención de los ángeles, que ciegan a la turba y rescatan a Lot, constituye no solo un milagro de protección sino un juicio divino sobre un sistema social que había institucionalizado la opresión.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">La escena: Lot en la puerta y la llegada de los huéspedes</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El relato comienza con un detalle de enorme peso simbólico: "Llegaron, pues, los dos ángeles a Sodoma a la caída de la tarde; y Lot estaba sentado a la puerta de Sodoma" (Génesis 19:1). En el mundo del Antiguo Oriente Próximo, sentarse a la puerta de la ciudad no era un acto casual. Era el lugar donde se administraba justicia, se cerraban contratos y se ejercía el liderazgo cívico (Rut 4:1-11; Proverbios 31:23). Lot, el sobrino de Abraham que había venido como extranjero, se ha convertido en parte del establecimiento judicial de Sodoma.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Cuando Lot ve a los dos viajeros, "se levantó a recibirlos, y se inclinó hacia el suelo" (Génesis 19:1). Este gesto de humildad no es mera cortesía: en la cultura del Antiguo Oriente Próximo, la hospitalidad hacia el forastero era un deber sagrado, uno de los pocos mecanismos que protegían a quienes viajaban sin la cobertura de un clan o un estado. La insistencia de Lot es notable: "os ruego que no paséis de largo" (Génesis 19:2), y los presiona hasta que aceptan. Su urgencia sugiere que Lot conocía bien el peligro que corrían los extranjeros en Sodoma después del anochecer.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Los ángeles aceptan, y Lot les prepara una comida. Pero antes de que puedan descansar, la ciudad se moviliza.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">El cerco: "Todo el pueblo junto, desde el más joven hasta el más viejo"</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>"Pero antes de que se acostasen, rodearon la casa los hombres de la ciudad, los varones de Sodoma, todo el pueblo junto, desde el más joven hasta el más viejo" (Génesis 19:4). El texto bíblico es enfático en la totalidad del asedio: no es una pandilla marginal ni un grupo de delincuentes actuando en las sombras. Es la asamblea plena de la ciudad, unánime y corporativa. No hay disidentes, no hay quien se oponga, no hay voz de moderación entre los ciudadanos de Sodoma. La violencia contra el extranjero es, literalmente, la única política pública que concita consenso absoluto.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La demanda de la turba es brutal y explícita: "Sácalos, para que los conozcamos" (Génesis 19:5). El verbo hebreo <em>yada</em> ("conocer") tiene en este contexto una connotación sexual inconfundible, como en otros pasajes del Antiguo Testamento (Génesis 4:1, 17, 25). Pero exigir la violación de unos hombres por parte de toda la asamblea de la ciudad trasciende el ámbito de lo privado. Es un acto de dominación política: se trata de humillar, despojar de dignidad y enviar un mensaje a cualquier extranjero que pudiera considerar Sodoma como un lugar de tránsito o comercio. En las culturas del antiguo Oriente Próximo, la penetración forzada de un varón era el castigo definitivo para enemigos y traidores, un acto que despojaba a la víctima de todo estatus social y capacidad de reclamo legal.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La violencia que Sodoma proyecta sobre sus huéspedes no es lujuria; es terrorismo de Estado ejercido por la asamblea popular.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Lot entre la espada y la pared: hospitalidad contra violencia institucional</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>Lot sale a enfrentar a la turba y cierra la puerta tras de sí. Su primer gesto es de súplica: "Hermanos míos, ahora os ruego que no hagáis mal" (Génesis 19:7). Llamarlos "hermanos" es un intento de apelar a un vínculo de ciudadanía compartida, pero la turba ya ha dejado claro que no reconocen a Lot como igual.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>En la versión de la Reina-Valera 1960, Lot continúa: "He aquí ahora yo tengo dos hijas que no han conocido varón; os las sacaré fuera, y haced de ellas como bien os pareciere; solamente que a estos varones no hagáis nada, pues que vinieron a la sombra de mi tejado" (Génesis 19:8). Esta oferta ha sido objeto de intenso debate teológico. Sin embargo, la Traducción de José Smith (TJS) presenta una versión radicalmente distinta del episodio, donde es la turba quien exige a las hijas y Lot se niega a entregarlas: "Dios no justificará a su siervo en esto" (TJS, Génesis 19:11-14). La TJS reivindica la integridad moral de Lot y armoniza este pasaje con el testimonio de Pedro, quien llama a Lot "el justo Lot" (2 Pedro 2:7).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La respuesta de la multitud a la intervención de Lot revela las dinámicas de poder en juego: "Vino este aquí para habitar como extranjero, ¿y habrá de erigirse en juez? Ahora te haremos a ti más mal que a ellos" (Génesis 19:9). La turba recuerda a Lot su condición de forastero (<em>ger</em> en hebreo: residente extranjero con estatus legal limitado). Le niegan la autoridad moral para juzgarlos y, acto seguido, amplían la amenaza para incluir al propio Lot. La violencia que habían reservado para los huéspedes ahora se extiende al anfitrión. La escena ilustra la tragedia de la integración: Lot ha llegado a ser juez en Sodoma, pero en el momento crítico la ciudad le recuerda que nunca dejará de ser un extranjero.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">La intervención de los ángeles: ceguera y rescate</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>En el momento de máxima tensión, cuando la turba se precipita contra la puerta para derribarla, ocurre el punto de inflexión: "Entonces los varones extendieron la mano, y metieron a Lot en casa con ellos, y cerraron la puerta. Y a los hombres que estaban a la puerta de la casa hirieron con ceguera, desde el menor hasta el mayor, de manera que se fatigaban buscando la puerta" (Génesis 19:10-11).</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La intervención es quirúrgica y deliberada. Los ángeles no destruyen a la turba ni la masacran; simplemente la incapacitan. La ceguera —temporal y selectiva— cumple varias funciones narrativas y teológicas: protege a Lot y a sus huéspedes sin ejecutar el juicio final antes de tiempo, expone la impotencia de la violencia organizada frente al poder divino, y convierte a la turba en un objeto de ironía trágica —los violentos, que habían rodeado la casa para ejercer su poder, quedan reducidos a una multitud desorientada que tropieza en la oscuridad.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>La Traducción de José Smith añade un detalle que la Reina-Valera no contiene: "los ángeles de Dios, que eran varones santos, extendieron la mano y metieron a Lot en casa con ellos, y cerraron la puerta" (TJS, Génesis 19:15). La calificación de "varones santos" distingue explícitamente a los visitantes de los hombres comunes y subraya la naturaleza divina de la protección que reciben Lot y su familia.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">El significado del asalto en el conjunto del relato</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El asalto a la casa de Lot no es un incidente aislado dentro de la narrativa de Sodoma; es la culminación de un patrón que el texto ha venido anunciando desde Génesis 13. Cuando Abraham intercede por las ciudades, el "clamor" que ha subido hasta Dios (Génesis 18:20-21) sugiere que hay víctimas cuya voz clama justicia. El asalto a la casa de Lot revela quiénes son esas víctimas: los extranjeros, los forasteros, los que no tienen protección legal en una sociedad que ha institucionalizado la violencia contra el diferente.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El apóstol Pedro, al reflexionar sobre este episodio, describe a Lot como "el justo Lot, abrumado por la conducta licenciosa de los malvados" y afirma que "afligía cada día su alma justa al ver y oír los hechos inicuos de ellos" (2 Pedro 2:7-8). La palabra que Pedro usa para describir la vida de Lot en Sodoma —<em>afligía</em>, <em>tormentaba</em>— indica un sufrimiento continuo y profundo. Lot no era cómplice de Sodoma; era una víctima moral atrapada en un sistema que no podía cambiar.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El profeta Ezequiel diagnosticó siglos después la raíz del mal de Sodoma: "He aquí que esta fue la maldad de Sodoma tu hermana: soberbia, saciedad de pan, y abundancia de ociosidad tuvieron ella y sus hijas; y no fortaleció la mano del afligido y del menesteroso" (Ezequiel 16:49). La conexión entre la "abundancia de ociosidad" y la violencia organizada contra el extranjero no es casual: una sociedad que vive en la opulencia y no tiene ocupación productiva canaliza su energía colectiva hacia la opresión del débil. El asalto a la casa de Lot es la manifestación concreta de ese diagnóstico profético.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Judas, por su parte, añade otra capa de significado: "Como Sodoma y Gomorra y las ciudades vecinas, las cuales de la misma manera que aquellos, habiendo fornicado e ido tras carnes extrañas, fueron puestas por ejemplo, sufriendo el castigo del fuego eterno" (Judas 1:7). La expresión "carnes extrañas" (<em>sarkos heteras</em> en el griego original) apunta a la transgresión de los límites establecidos por Dios —no solo en el ámbito sexual, sino en la violación del orden creado que distingue al huésped del agresor, al extranjero del ciudadano, al justo del impío.</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Conclusión</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El asalto a la casa de Lot es el momento en que Sodoma revela su verdadero rostro. No es una ciudad caída por pecados privados, sino por una violencia pública, organizada y unánime contra el extranjero. La intervención de los ángeles —que ciegan a la turba y rescatan a Lot— es un juicio divino en miniatura: Dios pone un límite a la opresión institucionalizada. La ceguera de la turba es simbólica: una sociedad que ha cerrado los ojos a la justicia termina por perder la vista física también.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Para el lector moderno, el relato ofrece una advertencia que trasciende su contexto antiguo. Cada sociedad debe preguntarse: ¿quiénes son los "extranjeros" en nuestras puertas? ¿Nuestra comunidad protege al vulnerable o se organiza contra él? ¿Somos como Lot, que a pesar de sus complicaciones morales mantenía un núcleo de justicia, o como la turba de Sodoma, que encontró en la violencia contra el diferente su única expresión de unidad cívica? El asalto a la casa de Lot no es solo un relato antiguo; es un espejo.</p>
