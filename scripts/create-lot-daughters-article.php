<!-- /wp:quote -->

<!-- wp:paragraph -->
<p>Las diferencias son profundas y teológicamente significativas:</p>
<!-- /wp:paragraph -->

<!-- wp:list -->
<ul><!-- wp:list-item -->
<li><strong>La iniciativa cambia:</strong> En la KJV, Lot ofrece a sus hijas voluntariamente. En el JST, la turba exige a las hijas, y Lot se opone.</li>
<!-- /wp:list-item -->
<!-- wp:list-item -->
