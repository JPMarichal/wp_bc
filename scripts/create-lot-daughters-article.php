<h2 class="wp-block-heading">El texto de la Reina-Valera y su dificultad moral</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>En la versión castellana tradicional de Génesis 19:8, Lot se dirige a los hombres de Sodoma que rodean su casa y les dice: "He aquí ahora yo tengo dos hijas que no han conocido varón; os las sacaré fuera, y haced de ellas como bien os pareciere; solamente que a estos varones no hagáis nada, pues que vinieron a la sombra de mi tejado". La oferta de Lot —entregar a sus propias hijas vírgenes para ser violadas por la turba con tal de proteger a sus invitados— choca frontalmente con la sensibilidad moral moderna y antigua por igual.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Para empeorar las cosas, el apóstol Pedro se refiere a Lot como "el justo Lot, abrumado por la conducta licenciosa de los malvados" y afirma que "afligía cada día su alma justa al ver y oír los hechos inicuos de ellos" (2 Pedro 2:7-8). ¿Cómo puede un hombre que ofrece a sus hijas vírgenes a una turba violenta ser considerado "justo" por el testigo apostólico? Esta contradicción aparente ha llevado a muchos intérpretes a conclusiones forzadas sobre la moralidad de Lot o a simplemente ignorar el problema.</p>
