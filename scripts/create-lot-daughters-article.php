<p>Uno de los episodios más inquietantes del relato de Sodoma y Gomorra es la aparente disposición de Lot a entregar a sus hijas a la turba que exigía a sus huéspedes. En la versión de la Reina-Valera 1960, Lot dice: "He aquí ahora yo tengo dos hijas que no han conocido varón; os las sacaré afuera, y haced con ellas como bien os pareciere" (Génesis 19:8). Esta oferta ha desconcertado durante siglos a lectores y teólogos, pues parece incompatible con el carácter de un hombre a quien el apóstol Pedro llama "justo Lot" (2 Pedro 2:7). La Traducción de José Smith (JST) de Génesis 19:9-15 resuelve esta dificultad al revelar un texto significativamente diferente que reivindica la integridad moral de Lot.</p>
