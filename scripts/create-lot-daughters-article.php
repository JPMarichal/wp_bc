<h2 class="wp-block-heading">La Traducción de José Smith: un cambio textual significativo</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>La Traducción de José Smith (JST) de Génesis 19:9-15 —que puede consultarse en el Apéndice JST de la edición SUD de la Biblia— presenta una versión radicalmente diferente del intercambio entre Lot y los hombres de Sodoma. En lugar de mostrar a Lot ofreciendo a sus hijas, el JST aclara que fue la turba quien exigió tanto a los huéspedes como a las hijas, y que Lot se negó firmemente a entregarlas.</p>
<!-- /wp:paragraph -->

