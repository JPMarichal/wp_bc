<h2 class="wp-block-heading">Lot como "hombre justo" en las Escrituras</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El testimonio de Pedro sobre el carácter de Lot adquiere coherencia plena a la luz del JST. Pedro no solo llama a Lot "justo" en dos ocasiones, sino que describe su vida en Sodoma como un tormento espiritual diario: "afligía cada día su alma justa al ver y oír los hechos inicuos de ellos" (2 Pedro 2:8). Esta descripción es incompatible con un hombre que sacrificaría a sus hijas para salvarse a sí mismo, pero perfectamente compatible con un hombre que resiste la presión social y moral de una ciudad corrupta.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>El JST también armoniza el carácter de Lot con otros detalles del relato que la KJV deja sin resolver. Por ejemplo, los ángeles tienen que tomar a Lot de la mano para sacarlo de Sodoma (Génesis 19:16), pero la razón de su vacilación no es la malicia o el apego al pecado —como a veces se ha interpretado— sino posiblemente la angustia de dejar atrás a sus yernos, a sus posesiones, y a una comunidad donde, a pesar de todo, él había ocupado una posición de liderazgo cívico como juez en la puerta de la ciudad (Génesis 19:1, 9).</p>
<!-- /wp:paragraph -->

<!-- wp:heading -->
<h2 class="wp-block-heading">Las hijas de Lot después de la destrucción</h2>
<!-- /wp:heading -->

<!-- wp:paragraph -->
<p>El JST también se pronuncia sobre el episodio posterior a la destrucción, cuando las hijas de Lot embriagan a su padre y conciben hijos de él (Génesis 19:30-38). La Traducción de José Smith añade el calificativo de "inicuas" a las acciones de las hijas (JST, Génesis 19:37, 39; véanse las notas al pie en Génesis 19:31 y 19:35 de la edición SUD), dejando claro que Lot no fue cómplice ni responsable de ese acto, sino víctima de un engaño planeado por ellas. Esta distinción es importante porque exonera aún más a Lot de cualquier culpa en los eventos que rodearon la disolución de su familia.</p>
<!-- /wp:paragraph -->

<!-- wp:paragraph -->
<p>Es cierto que la actitud de las hijas refleja la profunda influencia que la cultura de Sodoma había ejercido sobre ellas, a pesar de los esfuerzos de su padre por mantener la integridad en medio de la corrupción. Pero el texto del JST deja claro que la iniciativa y la responsabilidad moral recaen sobre ellas, no sobre Lot.</p>
<!-- /wp:paragraph -->

